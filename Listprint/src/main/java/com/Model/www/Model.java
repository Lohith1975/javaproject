package com.Model.www;

public class Model {
	private String name;
	private int age;
	public Model(String name, int age) {
//		super();
		this.name = name;
		this.age = age;
	}
	public Model() {}
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	

}
